// Number
let age = 22;
console.log("Age:", age);
console.log("Type:", typeof age);

// String
let name = "Sai Reddy";
console.log("Name:", name);
console.log("Type:", typeof name);

// Boolean
let isStudent = true;
console.log("Is Student:", isStudent);
console.log("Type:", typeof isStudent);

// Undefined
let city;
console.log("City:", city);
console.log("Type:", typeof city);

// Null
let phone = null;
console.log("Phone:", phone);
console.log("Type:", typeof phone);

// BigInt
let population = 9876543210123456789n;
console.log("Population:", population);
console.log("Type:", typeof population);

// Symbol
let id = Symbol("userID");
console.log("ID:", id);
console.log("Type:", typeof id);

// Object
let student = {
    name: "Sai Reddy",
    age: 22,
    course: "JavaScript"
};
console.log("Student:", student);
console.log("Type:", typeof student);

// Array (Object)
let subjects = ["HTML", "CSS", "JavaScript"];
console.log("Subjects:", subjects);
console.log("Type:", typeof subjects);

// Function (Object)
function greet() {
    return "Welcome to JavaScript!";
}
console.log(greet());
console.log("Type:", typeof greet);